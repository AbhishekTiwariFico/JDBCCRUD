package com.dataholder;

public class DataContainer {
     public  int id;
     public int age;
    public String name;
     public String state;
     public String department;
     public String qualification;


    public DataContainer(int id,int age,String name,String state,String department,String qualification) {
        this.id = id;
        this.age = age;
        this.name =name;
        this.state = state;
        this.department = department;
        this.qualification = qualification;
    }
}
